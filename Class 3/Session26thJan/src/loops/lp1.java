package loops;

public class lp1 {
// demo of while loop
	public static void main(String[] args) {
		int x = 1;
		while(x<=5)
		{
			System.out.println(x + " Hello World");
			x++;
		}
		System.out.println("-------------");
		x = 5;
		while(x>=1)
		{
			System.out.println(x + " Hello World");
			x--;
		}
	}

}
